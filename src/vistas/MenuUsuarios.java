    private void RecargarUsuario() {
        String sql = "SELECT id, nombre, correo, rol FROM usuarios"; 
        try (Connection conn = conectarDB();
            PreparedStatement ps = conn.prepareStatement(sql); 
            ResultSet rs = ps.executeQuery()) {  // Usamos executeQuery() con el PreparedStatement

            // Crear el modelo para la tabla
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Usuario");
            model.addColumn("Correo");
            model.addColumn("Rol");

            // Recorrer el ResultSet y llenar la tabla
            while (rs.next()) {
                Object[] row = new Object[4];
                row[0] = rs.getInt("id");          // Columna 'id'
                row[1] = rs.getString("Nombre");  // Columna 'Nombre'
                row[2] = rs.getString("correo");   // Columna 'Correo'
                row[3] = rs.getString("rol");      // Columna 'rol'
                model.addRow(row);                 // AÃ±adir la fila a la tabla
            }

            // Asignar el modelo a la tabla
            TableUsuarios.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de los usuarios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }



